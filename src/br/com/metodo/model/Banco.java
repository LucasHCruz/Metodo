package br.com.metodo.model;

public class Banco
{
	private String codigo;
	private String banco;
	
	public String getCodigo()
	{
		return codigo;
	}
	
	public void setCodigo(String codigo)
	{
		this.codigo = codigo;
	}
	
	public String getBanco()
	{
		return banco;
	}
	
	public void setBanco(String banco)
	{
		this.banco = banco;
	}
}